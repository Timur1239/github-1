// Методы массива

// push - добавляет новый элемент в конец
// unshift - добаляет новый элемент в начало
// pop - удаляет конец элемента
// shift - удаляет начало элемента


// toUpperCase - заглавные буквы
// toLowerCase - маленькие буквы
// reverse - меняет слова задом на перед
// sort - сортирует

let cars = ['bmw', 'amg', 'bugatti']
cars.push('Ferrari');
console.log(cars);

let laptop =['asus', 'hp', 'acer',]
laptop.unshift('lenovo')
console.log(laptop)

let phone = ['apple', 'lg', 'nokia', 'samsung']
phone.pop()
console.log(phone)

let game = ['standoff 2', 'free fire', 'roblox']
game.shift()
console.log(game)

let arr = 'Hello world!'
console.log(arr.toUpperCase())

let arr1 = 'Hello world!'
console.log(arr1.toLowerCase())

let latin = ['a', 'b', 'c', ]
latin.reverse()
console.log(latin)

let number = ['3', '1', '2', ]
number.sort()
console.log(number)


